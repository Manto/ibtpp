蜂蜜是一个已知的可能携带产生婴儿肉毒症的梭状芽孢杆菌孢子的食品。虽然说大多数婴儿肉毒症的患者不是因为接触蜂蜜才感染该疾病的，但是这是目前唯一可以避免的细菌源。多年来，研究人员将数百种食品、饮料及其他可能进入婴儿口腔的物品进行测试排查，最后研究结果表明，大多数婴儿肉毒症的患者感染的途径是吞食了携带孢子的微小灰尘颗粒。

<br><br> 
美国加州公共卫生署（CDPH）制作了一个有关蜂蜜的信息册，来提醒家长们避免给一岁以下婴儿喂食蜂蜜。该信息册是可以随意下载、打印以及分发的，并且加州卫生服务署也鼓励该信息册的传播。该信息册没有版权，加州公共卫生署鼓励大家下载、打印以及分发。要下载这个避免给一岁以下婴儿食用蜂蜜的教育信息册可点击下方的链接（PDF模式）。
