<b>现在唯一已知的预防措施是避免给小于12个月的婴儿食用蜂蜜。母乳喂养可以有效地减缓病发时间。
</b><br><br>
使用母乳还是配方奶喂养婴儿是不是一个诱发病情发展的因素仍然悬而未决。然而，到目前为止，研究表明是否用母乳喂养和婴儿肉毒症患者是有着直接联系的<sup>2，4,15,16,19,23,25</sup>。但是基于研究结果现在却有两种观点：一种认为用母乳喂养使婴儿更容易发病<sup>15,16,23</sup>，而另一种观点则认为母乳延缓了发病时间，使得患者有时间住院治疗<sup>1-4</sup>。配方奶喂养婴儿的病发年龄（平均7.6周大）明显较小，大约是母乳喂养的婴儿（平均13.8周大）的二分之一。此外，因患突发性婴儿肉毒症而停止呼吸在家中死亡的患者均为配方奶喂养的婴儿。这些都反映出母乳与配方奶的一些成分不同对感染婴儿肉毒症的几率以及病情轻重都有影响。这可能是因为肠道中适合肉毒梭状芽孢杆菌孢子生长的小生态环境的差异，可能是因为母乳在配方奶增强免疫力方面的差异（例如母乳中含有配方奶中没有的乳铁蛋白和分泌型IgA）<sup>9</sup>，也可能是其他未知原因。

<br><br> 
<b>实验室分析和流行病学研究都表明，蜂蜜是迄今为止所发现的一个适合肉毒梭状芽孢杆菌孢子的载体</b><sup>5-7,10,11,13,18,22,24</sup>。现在，已在35例世界各地的实例中检测出病因是带有肉毒梭状芽孢杆菌孢子的蜂蜜。在每一例实例中，当时蜂蜜中发现的肉毒杆菌毒素的类型（A或B）和造成婴儿的疾病的肉毒杆菌毒素的类型（A或B）都完全一致。这只是个巧合的概率小于一百亿分之一。而且，发现带有肉毒杆菌孢子的蜂蜜来自美国、阿根廷、澳大利亚、加拿大、中国、台湾、丹麦、芬兰、意大利、挪威、西班牙、日本和中美洲等世界各地<sup>6,7,10,11,13,17,18,20,22,24</sup>（除了在英国的蜂蜜中并未发现肉毒杆菌孢子8）。综上所述，因为蜂蜜也不是必不可少的营养品，美国各大儿科诊所、公共卫生和蜂蜜行业都达成共识不再给婴儿喂食蜂蜜。英国和美国分别在1996年和2000年正式在多个蜂蜜品牌的所有产品上开始附上警告标签，警告家长不要给婴儿喂食蜂蜜。

<br><br>
关于玉米糖浆与婴儿肉毒症的联系源于两份报告。1982年，美国食品和药物管理局（FDA）发现在事先未曾开启的浅色和深色玉米糖浆零售样品中有大约0.5%的样品（961份样品中的5份）含有肉毒梭状芽孢杆菌B型孢子<sup>13</sup>，后来制造商改变了生产方法。1989年，美国疾病控制与预防中心（CDC）报告了一项历时两年的流行病学研究结果，该项研究涵盖了除加州外的美国各州的病例<sup>23</sup>。研究人员把患者按照年龄分组，使用逻辑回归建模技术得出了食用玉米糖浆、用母乳喂养和发病年龄至少两个月大这三项变量之间的数据联系<sup>21,23</sup>。

<br><br> 
与上述报告不同，加拿大1988年的一项调查中在43个玉米糖浆样品中没有发现任何肉毒梭状芽孢杆菌孢子<sup>10</sup>。美国食品与药物管理局1991年对738个糖浆样品（354个浅色玉米糖浆样品，271个深色玉米糖浆样品）进行的市场调查中也未发现肉毒梭状芽孢杆菌孢子<sup>14</sup>。最后，1979年的一项流行病学研究对比了41名患病婴儿和107名健康婴儿食用玉米糖浆的情形，得出的结论是玉米糖浆具有显著的保护作用，能够降低A型婴儿肉毒症的发病率<sup>6</sup>。对这种结论的解释是：如果家长选择玉米糖浆作为增甜剂，就不太可能同时也使用蜂蜜作为增甜剂。<b>因此，就目前的研究结果来看，玉米糖浆不是肉毒梭状芽孢杆菌孢子的来源或婴儿肉毒症的致病因素。</b>

<br><br> 
<b>必须强调的是，在大多数婴儿肉毒症病例中并没有找到肉毒梭状芽孢杆菌孢子的来源。在这些病例中，染病的原因极有可能是吞食了空气中微小灰尘颗粒（肉眼看不见）携带的孢子。</b>

