La miel es el único depósito de alimentos identificado y evitable de <i>C. botulinum</i>,
la espora bacteriana que causa el botulismo del lactante. Si bien, en la
actualidad, la mayoría de los casos de botulismo del lactante no son causados
por la exposición a la miel previa a la enfermedad, es la única fuente evitable de
exposición a las bacterias que ha sido vinculada a los casos de botulismo del
lactante gracias a la evidencia de laboratorios y epidemiólogos (vea la página
sobre prevención). Mediante un proceso de exclusión (haciendo pruebas durante
años de cientos de alimentos, bebidas y otros productos colocados en las bocas
de los bebés con resultados negativos), se llegó a la conclusión de que la
mayoría de los pacientes con botulismo del lactante adquirían sus esporas al
tragar partículas de polvo microscópicas a través de las cuales se transportan
las esporas.

