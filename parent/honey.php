Honey is the one identified and avoidable food reservoir of <i>C. botulinum</i>, the bacterial spore that causes infant botulism.  While most cases of infant botulism today are not caused by exposure to honey prior to illness, it is the only avoidable source of exposure to the bacteria.  By a process of exclusion (testing over the years of hundreds of foods, beverages, and other items placed in infants' mouth with negative results), it was concluded that most infant botulism patients acquired their spores by swallowing microscopic dust particles on which the spores travel.
<br><br> 
The California Department of Public Health (CDPH) prepared an informative brochure to increase awareness of avoiding feeding honey to infants under one year of age.  The pamphlet is not copyrighted and CDPH encourages any and all interested parties to download, print, and distribute this brochure.
