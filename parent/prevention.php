<b>
The only known prevention measure for infant botulism is to avoid feeding honey to infants. Breastfeeding may slow the onset of illness if it develops. After 12 months of age, most individuals are not susceptible to botulism from honey ingestion.
</b>
<br><br>

The role of breastfeeding and formula feeding as factors possibly predisposing to
the development of illness remains unsettled. All studies to date have identified
an association between being breast fed and being hospitalized for infant
botulism 1-7 . This finding has resulted in one perspective that holds that
breastfeeding predisposes to the development of illness <sup>3, 4, 6</sup>, whereas the other
perspective holds that breast-feeding slows its onset sufficiently to permit
hospitalization to occur <sup>1, 2, 8, 9</sup>. However, among hospitalized patients, the mean
age at onset of infant botulism in formula-fed infants (7.6 weeks) was significantly
younger and approximately half that of breast-fed infants (13.8 weeks). In
addition, the fulminant-onset infant botulism patients who died at home all were
formula-fed. The relative susceptibility of formula-fed and breastfed infants to the
acquisition of infant botulism and the resultant severity of their disease may
reflect differences in the availability of suitable ecologic niches in the intestinal
flora for <i>C. botulinum</i>, differences in the availability of immune factors (such as
lactoferrin and secretory IgA) contained in human milk but not in formula milk, 9  or
other differences not identified yet.

<br><br> 

<b>Honey is an identified and avoidable food reservoir of <i>C. botulinum</i>. While
most cases of infant botulism today are not caused by exposure to honey
prior to illness, it is the only avoidable dietary source of exposure to the
bacterium that has been definitively linked to infant botulism by both
laboratory and epidemiologic evidence <sup>7, 10-18</sup>
</b>

<br /><br />

To date, 33 instances worldwide are known in which <i>C. botulinum</i> has been
identified in honey associated with infant botulism cases. In each instance, the
toxin type (A or B) of the spores in the honey matched the toxin type (A or B) of
the C. botulinum that caused the infant's illness; the probability that such perfect
concordance occurred by chance is less than 1 in 10 billion. <i>C. botulinum</i> spores
have been found in the honey from the United States, Argentina, Australia,
Canada, Central America, China, Taiwan, Denmark, Finland, Italy, Norway,
Spain, Japan, Kazakhstan, Lithuania, and Poland, <sup>18-30</sup>. <b>For these reasons, all
major pediatric, public health, and honey industry agencies in the United
States have joined in the recommendation that honey not be fed to
infants.</b> In 2000, several honey brands sold in the United States began to carry a
warning to not feed honey to infants; an equivalent label first appeared on British
honey in 1996.

<br /><br />

Consideration of the possible role of corn syrup in infant botulism is necessitated
by two reports. In 1982, the U.S. Food and Drug Administration (FDA) found <i>C.
botulinum</i> type B spores in approximately 0.5 percent (5 of 961) of previously
unopened retail samples of light and dark corn syrup <sup>23</sup>; the manufacturer then
made changes in the production process. In 1989, the CDC reported a 2-year
epidemiologic study of U.S. cases from all states except California <sup>6</sup>. By
subgrouping patients by age and using logistic regression modeling techniques,
researchers identified a statistical association between the triad of corn syrup
exposure, breastfeeding, and an age of 2 months or older at onset <sup>6, 31</sup>.

<br /><br />

In contrast to these reports, a 1988 Canadian survey found no <i>C. botulinum</i>
spores in 43 corn syrup samples <sup>21</sup>. A 1991 FDA market survey of 738 syrup
samples (354 of which were light corn syrup and 271 were dark corn syrup)
concluded that none contained <i>C. botulinum</i> spores <sup>32</sup>. In addition, a 1979
epidemiologic study that simply compared corn syrup exposure rates in 41 cases
and 107 control infants identified feeding of corn syrup as a significant protective
factor against the acquisition of type A infant botulism <sup>19</sup>. The explanation offered
for the latter observation was that if a parent chose corn syrup as a sweetener for
the infant, honey was unlikely to have been fed to the child as a second
sweetener.  <b>Thus, on the basis of evidence presently available, corn syrup
does not appear to be a source of <i>C. botulinum</i> spores or a risk factor for
the acquisition of infant botulism.</b>

<br /><br />

<i>C. botulinum</i> spores also have been identified in bulk and packaged chamomile
tea <sup>33</sup> and bulk linden flower tea <sup>34</sup> though there have been no reports to date with
definitive laboratory and epidemiological evidence linking a case of infant
botulism to herbal tea consumption.

<br /><br />

<b>It deserves emphasis that for most cases of infant botulism, no source
of <i>C. botulinum</i> spores is ever identified, even circumstantially. In these
cases, the illness was likely acquired by swallowing spores adherent to
airborne microscopic (invisible) dust.
</b>

