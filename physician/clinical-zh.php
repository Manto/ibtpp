<blockquote>
  <p><a name="History"><span class="emphasis1">历史</span></a></p>
  <p><font face="Arial">因为婴儿没有描述病情的能力，婴儿肉毒症的感染只能通过细心观察发现。一般首先出现的症状是便秘（或排便频率减少），但是因为这不是婴儿肉毒症独有的症状，这常常会被家长和医生忽略。容易被家长捕捉到的第一个症状是难以进食。母乳喂养的婴儿母亲可能发现孩子的吸允很微弱。婴儿变的疲倦和面无表情也同样是线索。另外，婴儿的呼吸可能会变得很急促，并且哭声微弱。流涎可能变得更明显，但这有时归因于牙，而不是到吞咽困难。</font>
  <br><br><a href="#top"><img src="http://www.infantbotulism.org/images/backtotop.gif" border="0" align="right" /></a></p>
  <blockquote>
    <blockquote>
      <p><font face="Arial">&nbsp;</font></p>
    </blockquote>
  </blockquote>
  <p><a name="Clinical Presentation"><span class="emphasis1">临床表现</span></a></p>
	<p><font face="Arial"> 一个没有表现出任何常规症状但确实患上严重的婴儿肉毒症的患者也曾出现过。详情可见<a href="journal_of_clinical_biology_excerpt.pdf" target="_blank">Nevas et al., Journal of Clinical Microbiology</a>,
2005; <a href="Mitchell_and_Tseng_Ong.pdf">Mitchell W, Tseng-Ong L, Pediatrics</a> 2005; 116;436-438; and <a href="Hurst_and_Marsh.pdf">Hurst and Marsh, The Journal of Pediatrics</a>, 122(6):909-911, 1993.
	</font>
  <p><font face="Arial">因为肉毒杆菌毒素结合于神经肌肉连接处，毒素会造成弛缓性运动麻痹，一般始于延髓肌肉，之后体肌肉组织受到影响。在疾病发展较快时，就诊患者可能会有全身无力、肌张力低下和呼吸困难等症状。</font></p>
  <p><font face="Arial">一个典型的婴儿肉毒症患者可能有以下的症状：上睑下垂（可能只有在婴儿的头被扶正的时候比较明显）、哭声虚弱、吸允微弱、难以吞咽、流涎、瞳孔扩张或反应迟钝、目光散漫、面无表情、难以控制头部、肛门括约肌张力低下、肌张力低下和整体的虚弱。另外，腱反射可能会变弱。患者的五感都是正常的，但是因为运动麻痹无法正常控制肌肉表达。在疾病的初期或当患病不严重的情况下，婴儿肉毒症的一些症状很容易被忽略。颅神经麻痹和肌肉功能的疲劳性都需要仔细观察才能发现。以下表格介绍了一些帮助确认症状的技巧。
</font></p>
<p><br><a href="#top"><img src="http://www.infantbotulism.org/images/backtotop.gif" border="0" align="right" /></a><br><br></p>

<blockquote>
  <p><span style="font-family: arial; font-size: 16px; font-weight: none;"><a
  name="Table 1. PHYSICAL EXAMINATION SIGNS HELPFUL IN THE DIAGNOSIS OF INFANT BOTULISM">表格1：对婴儿肉毒症的诊断有帮助的特征</span></a></p>
</blockquote>

<blockquote>
  <u><p><font face="Arial">方法一：</u> 将病人带到一个较暗的房间。对着病人的眼睛照一束亮光并观察病人瞳孔收缩的速度。瞳孔达到最小后，移开光源，等待瞳孔扩张至正常大小。重复同样的步骤满2-3分钟。<u>观察症状：</u>瞳孔收缩迟缓且无法最大限度收缩。另外，缩肌疲劳可能造成“伪”驼背。</font></p>
  <u><p><font face="Arial">方法二：</u> 对着眼睛凹处照一束光并维持1-3分钟，即使婴儿试图躲开。<u>观察症状：</u>
  潜在眼肌麻痹以及躲避光源的反应减少。婴儿肉毒症的典型临床症状就是因虚弱造成的难以维持重复性肌肉动作。另外观察因虚弱造成的四肢挪动减少。</font></p>
  <u><p><font face="Arial">方法三：</u>将手洗干净后，把小指轻轻伸入婴儿的嘴巴，注意不要堵到气管。观察反射性吸允的力度和持久度。<u>观察症状：</u> 吸允微弱并无法维持。若婴儿未进食，也可以迅速检查咽喉反射强度。</font>
  </p>
</blockquote>

