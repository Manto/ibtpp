<blockquote>
  <p><a name="History"><span class="emphasis1">History</span></a></p>
  <p><font face="Arial">Because infants are unable to describe their symptoms, the onset of
  infant botulism can only be detected by careful observation. Usually the first indication
  of illness is <i><b>constipation</b></i> (defined as decreased frequency in defecation),
  although this sign is frequently overlooked by parents and physicians. Generally, parents
  first notice that the baby feeds poorly. The breast-feeding mother may notice breast
  engorgement because the baby&#146;s suck is weak. The infant has become lethargic and
  listless. Respiratory effort may become shallow and rapid, and the cry is feeble. Drooling
  may become more noticeable, which is sometimes attributed to teething rather than to dysphagia.</font>
  <br><br><a href="#top"><img src="/images/backtotop.gif" border="0" align="right" /></a></p>
