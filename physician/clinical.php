  <p><a name="Clinical Presentation"><span class="emphasis1">Clinical Presentation</span></a></p>
	<p><font face="Arial"> A "catastrophic" presentation of infant botulism with a paucity of the usual clinical signs has also been recognized.  See <a href="journal_of_clinical_biology_excerpt.pdf" target="_blank">Nevas et al., Journal of Clinical Microbiology</a>,
2005; <a href="Mitchell_and_Tseng_Ong.pdf">Mitchell W, Tseng-Ong L, Pediatrics</a> 2005; 116;436-438; and <a href="Hurst_and_Marsh.pdf">Hurst and Marsh, The Journal of Pediatrics</a>, 122(6):909-911, 1993.
	</font>
  <p><font face="Arial">Because botulinum toxin binds at the neuromuscular junction, the
  toxin produces a flaccid motor paralysis that invariably begins in the bulbar musculature.
  Somatic musculature is affected next, and patients with rapidly evolving illness may have
  generalized weakness, hypotonia and respiratory difficulty when first seen.</font></p>
  <p><font face="Arial">On initial presentation, the typical patient has some or all of the
  following findings: ptosis (which may not be evident until the infant&#146;s head is held
  erect), weak cry, diminished suck and gag, drooling and/or pooling of saliva, dilated
  and/or sluggishly reactive pupils, disconjugate gaze, blunted facial expression, poor head
  control, decreased anal sphincter tone, hypotonia and generalized weakness. Deep tendon
  reflexes may be either normal or decreased. Sensation remains intact, but this may be
  difficult to demonstrate because of the motor paralysis.</font></p>
  <p><font face="Arial">In mild cases or in the early stages of illness, the physical signs
  of infant botulism may be subtle and easily overlooked. Cranial nerve palsies and the
  fatigability of muscular function must be elicited by careful examination. Table 1 below
  describes physical examination techniques which have proved helpful in evaluating these
  infants. </font>  <br><br><a href="#top"><img src="/images/backtotop.gif" border="0" align="right" /></a><br><br></p>

<blockquote>
  <p><span style="font-family: arial; font-size: 16px; font-weight: none;"><a
  name="Table 1. PHYSICAL EXAMINATION SIGNS HELPFUL IN THE DIAGNOSIS OF INFANT BOTULISM">Table
  1. PHYSICAL EXAMINATION SIGNS HELPFUL IN THE DIAGNOSIS OF INFANT BOTULISM</span></a></p>
</blockquote>

<blockquote>
  <u><p><font face="Arial">Test 1.</u> Take the patient to dark room.  Shine a bright light into the eye; note quickness of pupillary constriction. Remove the light when constriction is maximal; let the pupil dilate. 
  Then immediately repeat, continuing for 2&#150;3&nbsp;minutes. <u>Findings:</u>
  The initially brisk pupillary constriction may become sluggish and unable to constrict maximally.  Also, constrictor muscle fatigability may yield a &quot;pseudo&quot; gibbus.</font></p>
  <u><p><font face="Arial">Test 2.</u> Shine a bright light onto fovea, keeping it there for
  1&#150;3&nbsp;minutes even if the infant tries to deviate her/his eyes. <u>Findings:</u>
  Latent ophthalmoplegia may be elicited, and/or purposeful efforts to avoid the light may
  diminish, because fatigability with repetitive muscle activity is the clinical hallmark
  of botulism. Also observe for initial squirming of the extremities that may diminish because of fatigability.</font></p>
  <u><p><font face="Arial">Test 3.</u> Place a clean fifth finger in the infant&#146;s
  mouth, taking care not to obstruct the airway. Note the strength and duration of the
  reflex sucking. <u>Findings:</u> The suck is weak and poorly sustained. The gag reflex strength also may be quickly checked (if the infant has not been fed recently).</font>
  </p>
</blockquote>

