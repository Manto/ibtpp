<div style="max-width: 800px; margin: auto; ">
<p>We are very sorry to share with you the unexpected, sad news of the passing of Dr. Steve Arnon, Chief of the Infant Botulism Treatment and Prevention Program.  Dr. Arnon passed away peacefully in his sleep on August 17, 2022, while on a hiking trip with lifelong friends in Wyoming. In co-discovering, diagnosing, researching, and treating infant botulism for more than 45 years, Dr. Arnon saved thousands of lives here in California and around the world.</p>

<p>Dr. Arnon grew up in the Bay Area, cultivating his love for the outdoors and scholarship. As an adolescent he saved the large sum required to become a life member of the Sierra Club, for which he led wilderness outings for ten summers. He received his A.B. in biochemistry from Harvard College (magna cum laude), M.P.H. from Harvard School of Public Health, and M.D. from Harvard Medical School. While in Massachusetts, he balanced studies with adventures in the Harvard Mountaineering Club.</p>

<h2 style="text-align: center; text-decoration: underline;">Birth of the Infant Botulism Treatment and Prevention Program and the creation of BabyBIG<sup>&reg;</sup></h2>

<p> “In the fields of observation chance favors only the prepared mind” – Louis Pasteur (a favorite quote of Dr. Arnon's)</p>

<p>Steve returned to the Bay Area as a CDC EIS Fellow stationed at the California Department of Health Services. One weekend in March 1976, he received a call about a paralyzed infant from Salinas. Through rigorous epidemiological and laboratory investigation, Steve and colleagues collected the novel evidence that the baby’s digestive tract was colonized with Clostridium botulinum, the bacterium that produces an exceptionally potent neurotoxin. As additional infants with acute weakness in California were detected throughout 1976, Steve and his co-discoverers named the condition infant botulism, in contrast to botulism resulting from contaminated food or wounds.</p>

<img src="tribute/image5.jpg" align="left" style="max-width: 25%; padding: 10px;" />

<p>After fellowship, Steve founded what would become the Infant Botulism Treatment and Prevention Program (IBTPP), a unit of the California Department of Public Health (CDPH). Through his research, he characterized the distinct pathophysiology, epidemiology, clinical features, and risk factors of infant botulism. He led the discoveries that honey can contain spores of C. botulinum, and that exposure to honey is a risk factor for some cases of infant botulism. His relentless efforts to alert the public to avoid feeding honey to infants were joined internationally by pediatric and public health authorities, eventually resulting in voluntary labelling of commercial honey in the US.</p>


<p>Steve’s mountaineering required determination, fortitude, and grit, also requisite for his landmark development and licensure of Botulism Immune Globulin Intravenous (Human) (BIG-IV), tradename BabyBIG<sup>&reg;</sup>, the first and only licensed human botulism immune globulin in the world. It has been distributed to all 50 United States and worldwide to treat over 3,000 infants to date.</p>

<img src="tribute/image4.jpg" align="right" style="max-width: 30%; padding: 5px;" />

<p>Steve articulately described his arduous journey in the <a href="https://www.infantbotulism.org/readings/Peds_Creatn_Devlpmt_BIG_IV_apr07.pdf" target="_blank">Creation and Development of the Public Service Orphan Drug Human Botulism Immune Globulin</a>. Following the passage of the federal Orphan Drug Act of 1983, he received funding from the FDA Office of Orphan Products Development (OOPD) to conduct the pivotal clinical trial of Human Botulism Immune Globulin Intravenous (BIG-IV) in California from 1992-1997. This was followed by a six-year nationwide open-label study of BIG-IV from 1997-2003, culminating in FDA licensure of BIG-IV as BabyBIG<sup>&reg;</sup> on October 23, 2003. BIG-IV safety and efficacy data were summarized in the <a href="https://www.infantbotulism.org/readings/nejm-big.pdf" target="_blank">New England Journal of Medicine</a>. BabyBIG is the standard of care for infant botulism and shortens hospital stay by an average of 3.5 weeks, resulting in an average decrease in hospital charges of approximately $88,900, per patient (<a href="https://infantbotulism.org/readings/Payne_2018_Efficacy%20of%20BIG-IV%2012%20years%20post-licensure.pdf" target="_blank">J Pediatr 2018;193:172-7</a>).</p>

<p>Steve was recognized by FDA in 1998 with the Wiley Medal and Commissioner’s Special Citation “[f]or commitment and determination to test Botulism Immune Globulin antitoxin for treatment of the life-threatening disease of infant botulism. In 2004 he received the National Organization for Rare Disorders' Therapeutic Achievement Award.” In 2011 he received the CDPH Public Health Recognition (“Lifetime Achievement”) Award. </p>

