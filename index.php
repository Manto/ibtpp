<p>Steve revered and was the longest-standing member of the collaborative Interagency
Botulism Research Coordinating Committee (IBRCC), attending his first meeting in Atlanta in 1976 and subsequently joining its Executive Committee. A careful, deliberate, and precise writer, Stephen was as driven to publish his program’s findings in the peer-reviewed literature as he was to pursue those findings.  He has over 75 peer-reviewed publications including textbook chapters about infant botulism, many of which originally helped to describe the disease. </p>

<img src="tribute/image2.jpg" align="right" style="max-width: 40%;" />

<p>At the IBTPP, he instilled three “axioms” in his staff: 1) Assume nothing; 2) Expect the unexpected; and 3) Never give up. His passion for teaching and mentoring was evident to all who knew him, and he poured his heart and life into the IBTPP.</p>

<p>Dr. Stephen S. Arnon left an indelible mark on science, medicine, public health, CDPH, and infant botulism patients and their families worldwide. Honoring his incredible life and legacy, the IBTPP will press on towards our mission to “provide and improve the treatment of infant botulism and to prevent infant botulism and related diseases.”</p>

