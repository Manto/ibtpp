<td>¿Hay consecuencias a largo plazo del botulismo infantil?</td>
</tr>

<tr>
<td valign="top"><img src="/images/qa-a.gif" border=0></td>
<td>Con la ausencia de complicaciones serias contraídas en el hospital, no. La prognosis
para los pacientes de botulismo infantil es la recuperación total y completa. La
recuperación resulta con el recrecimiento de las terminaciones nerviosas que podrán
mandar señales para que los músculos se contraigan. La toxina botulinum no penetra
en el cerebro, y por lo tanto los pacientes de botulismo infantil retienen toda la
inteligencia, habilidad atlética, habilidad musical, sentido del humor y terquedad con las
cuales nacieron.
</td>
</tr>

