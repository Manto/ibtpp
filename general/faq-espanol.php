<td valign="top"><img src="/images/qa-q.gif" border=0></td>
<td>¿Cómo contrae el bebé botulismo infantil?</td>
</tr>

<tr>
<td valign="top"><img src="/images/qa-a.gif" border=0></td>
<td>El bebé contrae el botulismo infantil al ingerir las esporas de botulismo en un
momento cuando el intestino grueso está vulnerable a la germinación de las esporas y
producción de la toxina. La ciencia médica aún no entiende todas las causas que hacen
un bebé susceptible a la germinación de esporas del botulismo. La miel se debe evitar
porque ha sido identificada como la única tipo de comida que contiene esporas de
botulismo. Por un proceso de exclusión, pruebas que se realizaron a través de un
periodo de años de muchos tipos de comidas, bebidas y otras cosas que fueron
puestas en las bocas de infantes que resultaron negativas, fue concluido que la
mayoría de los pacientes de botulismo infantil adquieren las esporas al ingerir
partículos microscópicos de polvo que contienen esporas.
</td>
</tr>

<tr><td colspan="2"><img src="/images/trans.gif" height=15></td></tr>
<tr>
<td valign="top"><img src="/images/qa-q.gif" border=0></td>
<td>Sé que la miel no se le debe dar a los bebés. ¿Puede una madre que amamanta su
bebé comer miel? ¿Puede el botulismo ser transmitido por leche materna? ¿Debería de
continuar amamantando mi bebé mientras que tenga botulismo infantil?</td>
</tr>

<tr>
<td valign="top"><img src="/images/qa-a.gif" border=0></td>
<td>No hay problema que la madre que está amamantando su bebé coma miel.
Botulismo no es transmitido por leche materna. El Programa de Prevención y
Tratamiento de Botulismo Infantil
recomienda que madres continúen amamantando sus bebés o darles leche materna
durante la enfermedad y recuperación del botulismo infantil.
<br /><br />
Es importante lavarse las manos cuidadosamente especialmente si personas cuidando
o viviendo con el bebé tienen cualquier tipo de contacto con miel o superficies que han
sidos expuestas a miel para reducer o eliminar la posibilidad que la boca del bebé
llegue a tener contacto con miel.
<br /><br />
Aunque esporas bacterianas que causan botulismo infantile se saben que de vez en
cuando están presentes en la miel, si la madre come esporas de botulismo presente en
la miel no se transmitirá en su leche maternal porque las esporas son muy grandes
para pasar por su pecho. La toxina botulinum no se transmita a la leche materna. Por
esto y otras razones, la leche materna no es un origen de esporas bacterianas o la
toxina que causa botulismo infantil. Si se dirige a la sección de cómo se cuida a los
bebés que sufren de botulismo infantil en nuestro sitio web, encontrará que la mejor
nutrición para el bebé es la leche materna. Madres deberían ser animadas y apoyadas
a continuar amamantando sus bebés mientras la enfermedad y recuperación.
<br /><br />
Para información más detallada sobre amamantar y el botulismo infantil, por favor vea
nuestra sección que trata con prevención.
</td>
</tr>

