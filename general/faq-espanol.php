<td valign="top"><img src="/images/qa-q.gif" border=0></td>
<td>¿Qual es la diferencia entre el botulismo infantil y el botulismo transmitido por comida?</td>
</tr>

<tr>
<td valign="top"><img src="/images/qa-a.gif" border=0></td>
<td>En el botulismo infantil las esporas del botulismo ingeridas se activan adentro del
intestino grande del bebé y producen la toxina botulinum. En el botulismo transmitido
por comida las esporas del botulismo se activan en la comida y producen la toxina
botulinum. Niños mayores y adultos contraen el botulismo transmitido por comida al
comer la comida donde la bacteria del botulismo ha producido la toxina botulinum.
</td>
</tr>

