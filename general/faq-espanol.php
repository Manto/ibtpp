<td valign="top"><img src="/images/qa-q.gif" border=0></td>
<td>¿Cómo debo lidiar con los pañales de mi bebe mientras él o ella se recupera de
botulismo infantil?</td>
</tr>

<tr>
<td valign="top"><img src="/images/qa-a.gif" border=0></td>
<td>Pacientes con botulismo infantil contienen <i>C. botulinum</i> y organismos en su
excremento durante un periodo de unas semanas a meses después de que contraen
botulismo infantil. Por esta razón es muy importante lavarse las manos muy bien
después de cambiar los pañales de su bebé. Los pañales deben ser tirados en la
basura rápidamente de manera que ninguna otra persona o animal (como una
mascota) pueda entrar en contacto con el pañal. Personas con heridas en sus manos
deben usar guantes disponibles al cambiar pañales.
<br /><Br />
Porque el paciente mientras su recuperación puede excretar la toxina botulinum y
organismos por semanas a meses después de contraer botulismo infantil, es
recomendable limitar contacto con otros infantes y niños pequeños durante este
tiempo. Ésto es para asegurarse de que otros niños no entren en contacto con el
excremento de un pañal sucio. Cualquier contacto que el paciente tenga con otros
niños durante este tiempo debe ser supervisado por un adulto.
<br /><Br />
<i>C botulinum</i> <b>no es</b> parte de la flora (bacteria normal y sana que viven dentro de
nuestros cuerpos) normal que se encuentra en el intestino del paciente. Eventualmente
<i>C. botulinum</i> será excretado en el excremento del infante.
</td>
</tr>

