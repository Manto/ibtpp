<tr>
<td valign="top"><img src="/images/qa-a.gif" border=0></td>
<td>No. Los niños tratados con BabyBIG ® tendrán un nivel protector de toxina-neutralizar
anticuerpos por lo menos seis meses después de la administración de la medicina.
Esta característica da suficiente tiempo para la eliminación de C. botulinum, que a
colonizado temporariamente el intestino del bebé causando la enfermedad. La
enfermedad del botulismo infantil primero fue reconocida hace más de 40 años y no
han habido casos de un bebé que adquiere la enfermedad más de una vez. Durante
este tiempo más de 4500 casos de botulismo infantil se saben haber ocurrido por todo
el mundo. Sin embargo, han habido algunos informes sobre bebés que recaien y
adquieren botulismo infantil nuevemente que han sido publicados durante este tiempo.
Revisión cuidadosa de estos informes por los doctores de nuestro programa
demuestran que en éstos casos los bebés fueron descargaron prematuramente antes
de que habían recuperado la fuerza para comer y respirar. Para más discusión sobre
este asunto vea por favor la sección de cómo cuidar a los pacientes con botulismo
infantil en nuestro sitio web debajo de la lengüeta que lee "Para Doctores".
