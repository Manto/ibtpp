<td>¿Mi bebé fue alimentado un producto comercial conteniendo miel (como cereal
hecho con miel), debe estar preocupado de botulismo infantil?</td>
</tr>

<tr>
<td valign="top"><img src="/images/qa-a.gif" border=0></td>
<td>Por favor refiérase a la Academia Americana de Pediatría que muestra las mejores
pautas al respecto a la alimentación de comidas sólidas para bebés <a href="http://www.healthychildren.org/
English/ages-stages/baby/feeding-nutrition" target="_blank">Academia
Americana de Pediatría Guías de las Prácticas Mejores sobre la alimentación y
nutrición infantiles</a>
<br /><br />
Muchos alimentos y productos comerciales contienen miel en muchas formas. Porque
los detalles de cada proceso de fabricación varían, el Departamento de Salud Pública
de California no puede comentar al respecto de la probabilidad que el producto
alimenticio que contiene miel puede contener las esporas viables de <i>C. botulinum</i>.
El Departamento de Salud Publica de California continúa recomendando no darle miel
o productos conteniendo miel a bebés bajo de la edad de un año.
</td>
</tr>

