<td>¿Qué es botulismo infantil?</td>
</tr>

<tr>
<td valign="top"><img src="/images/qa-a.gif" border=0></td>
<td>El botulismo infantil es la forma intestinal infecciosa de botulismo que resulta cuando
esporas ingeridas de la bacteria <i>Clostridium botulinum</i> colonizan el intestino grande del
bebé y producen la toxina botulinum. La toxina botulinum causa debilidad y pérdida de
tono muscular porque bloquea la capacidad de las terminaciones nerviosas de mandar
señales al músculo conectado para que se contraiga. Con frecuencia esta enfermedad
comienza con estreñimiento, pero usualmente se nota primero cuando el bebé muestra
dificultad en comer específicamente en succionar o tragando comida, un llanto débil o
diferente, y una expresión facial disminuida.
</td>
</tr>

