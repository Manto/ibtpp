<tr>
<td valign="top"><img src="/images/qa-q.gif" border=0></td>
<td>婴儿肉毒症对婴儿有没有什么长期影响？</td>
</tr>

<tr>
<td valign="top"><img src="/images/qa-a.gif" border=0></td>
<td>
如果在住院期间没有发生严重的并发症，则不会产生长期不利影响。几乎所有婴儿患者都是完全康复的。康复是源于神经末梢的更新，而新的神经末梢可以重新发出信号，指挥肌肉收缩。肉毒杆菌毒素不会侵入大脑，因此婴儿肉毒症患者的智力、肢体活动能力、音乐能力、幽默感和脾气都不会受到影响。
</td>
</tr>

<tr><td colspan="2"><img src="/images/trans.gif" height=15></td></tr>
<tr>
<td valign="top"><img src="/images/qa-q.gif" border=0></td>
<td>同一个婴儿可不可能第二次感染婴儿肉毒症？</td>
</tr>

<tr>
<td valign="top"><img src="/images/qa-a.gif" border=0></td>
<td>
不可能。使用BabyBIG<sup>&reg;</sup> 治疗的婴儿体内会拥有一层可以中和毒素的抗毒素，并维持至少六个月以上。而在这段时间内，可以保证肉毒梭状芽孢杆菌孢子（造成疾病的罪魁祸首）被彻底从体内清除。
<br /><br />
自从第一例婴儿肉毒症的案例在40年前被诊断出，数据表明在已知的4450余例婴儿肉毒症案例中（包含世界各地），没有任何患者第二次感染婴儿肉毒症。

<br /><br />
但是特别注意，根据几例特殊案例，当婴儿肉毒症的患者在未完全恢复哺乳和呼吸状况的情况下提前出院，是有可能导致婴儿肉毒症的复发。关于此内容的更进一步讨论请咨询“医生信息” 栏目下的“病人管理”。
</td>
</tr>



