<td>婴儿肉毒症如何治疗?</td>
</tr>

<tr>
<td valign="top"><img src="/images/qa-a.gif" border=0></td>
<td>
婴儿肉毒症的治疗方法是精心护理，特别注意婴儿的哺乳和呼吸需求。美国也采用一种叫BabyBIG® 的药物来缩短住院时间和减少并发症。
</td>
</tr>

<tr><td colspan="2"><img src="/images/trans.gif" height=15></td></tr>
<tr>
<td valign="top"><img src="/images/qa-q.gif" border=0></td>
<td>BabyBIG<sup>&reg;</sup>是什么?</td>
</tr>

<tr>
<td valign="top"><img src="/images/qa-a.gif" border=0></td>
<td>BabyBIG® 这种药物是一种克制肉毒杆菌毒素的抗毒素。在2003年10月23号，美国食品与药物管理局正式给BabyBIG® 授权，允许在婴儿肉毒症的治疗中被使用。使用BabyBIG® 可以大幅度简短住院时间，为患者家庭节省大笔住院费。
</td>
</tr>

<tr><td colspan="2"><img src="/images/trans.gif" height=15></td></tr>
<tr>
<td valign="top"><img src="/images/qa-q.gif" border=0></td>
<td>采用BabyBIG® 治疗后需要多长时间才能给孩子打预防针？</td>
</tr>

<tr>
<td valign="top"><img src="/images/qa-a.gif" border=0></td>
<td>再继续打预防针之前，婴儿的肌肉力量和张力必须恢复到正常水平。另外，活性病毒疫苗（如麻疹、腮腺炎、风疹和水痘等）需在使用BabyBIG<sup>&reg;</sup>治疗康复后等待六个月才能注射，因为药物BabyBIG®中的抗体可能会影响疫苗的有效性。
<br /><br />
轮状病毒的活性病毒疫苗（RotaTeq<sup>&reg;</sup> 和Rotatrix<sup>&reg;</sup>）需推迟到BabyBIG<sup>&reg;</sup>治疗后婴儿的肠道功能完全恢复之后。尽管现有数据并未证明BabyBIG<sup>&reg;</sup>会影响口服轮状病毒疫苗的功效，但是因为婴儿肉毒症会造成一段时间肠道蠕动过缓，得过婴儿肉毒症的患者请不要立刻接受轮状病毒的疫苗。
<br /><br />
同理，任何在BabyBIG<sup>&reg;</sup>治疗之前尚未给婴儿使用的轮状病毒疫苗（RotaTeq<sup>&reg;</sup> 和Rotatrix<sup>&reg;</sup>）需延迟给药。其他活性病毒疫苗（如麻疹、腮腺炎、风疹和水痘等）通常都是在一岁时首次注射，所以只有年龄在六个月或以上的婴儿肉毒症患者在接受BabyBIG<sup>&reg;</sup>治疗时才需要延迟接种此类疫苗。
</td>
</tr>


