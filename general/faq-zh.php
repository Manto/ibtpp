<td>什么是婴儿肉毒症？</td>
</tr>

<tr>
<td valign="top"><img src="/images/qa-a.gif" border=0></td>
<td>
婴儿肉毒症是一种能够传染的（肠道）肉毒杆菌中毒形式。当婴儿吞食一种病菌（肉毒梭状芽孢杆菌）的孢子后，这种病菌就会寄生于婴儿的大肠内，并产生肉毒杆菌毒素。肉毒杆菌毒素阻碍神经末梢通过讯号使与之相连的肌肉收缩的能力，导致失去肌肉张力身体软弱无力。该疾病通常始于便秘，但通常最初发现患病是由于哺乳困难（吸允或吞咽困难）、哭声微弱、以及面部表情减少。
</td>
</tr>

<tr><td colspan="2"><img src="/images/trans.gif" height=15></td></tr>

<tr>
<td valign="top"><img src="/images/qa-q.gif" border=0></td>
<td>婴儿肉毒症与食物传播肉毒杆菌中毒有什么区别？</td>
</tr>

<tr>
<td valign="top"><img src="/images/qa-a.gif" border=0></td>
<td>
两种都是由于肉毒梭状芽孢杆菌孢子产生的肉毒杆菌毒素，但是婴儿肉毒症中孢子是被婴儿吞食后在婴儿的大肠内萌发并产生毒素，而食物传播肉毒杆菌中毒当中孢子是在食物中萌发，产生肉毒杆菌毒素。年龄较大的儿童和成年人可能因为食入含有肉毒杆菌毒素的食品而感染食物传播肉毒杆菌中毒，但却不会感染婴儿肉毒症。
</td>
</tr>

<tr><td colspan="2"><img src="/images/trans.gif" height=15></td></tr>

<tr>
<td valign="top"><img src="/images/qa-q.gif" border=0></td>
<td>婴儿是怎么感染婴儿肉毒症的？</td>
</tr>

<tr>
<td valign="top"><img src="/images/qa-a.gif" border=0></td>
<td>
当婴儿误吞入肉毒梭状芽孢杆菌孢子，这些孢子会在婴儿的大肠中萌发并产生肉毒杆菌毒素，使婴儿感染婴儿肉毒症。医学研究尚未揭示肉毒梭状芽孢杆菌孢子在大肠中萌发所涉及的所有因素，但蜂蜜是一个已知并且可以避免的细菌源。多年来，研究人员将数百种食品、饮料及其他可能进入婴儿口腔的物品进行测试排查，最后研究结果表明，大多数婴儿肉毒症的患者感染的途径是吞食了携带孢子的微小灰尘颗粒。
</td>
</tr>

<tr>
<td valign="top"><img src="/images/qa-q.gif" border=0></td>
<td>我的婴儿食用过含有蜂蜜的产品（如含有蜂蜜的麦片），我需不需要担心他感染婴儿肉毒症？</td>
</tr>

<tr>
<td valign="top"><img src="/images/qa-a.gif" border=0></td>
<td>请参考美国儿科学院提供的给婴儿喂食固体的相关建议（点击
<a href="http://www.healthychildren.org/English/ages-stages/baby/feeding-nutrition" target="_blank">此处</a>).
<br /><br />
许多食物和商业产品都含有蜂蜜，但是因为各产品的制作方式可能不同，美国加州公共卫生署无法确定特定的含蜂蜜产品有没有肉毒梭状芽孢杆菌孢子隐藏在其中。
</td>
</tr>

<tr><td colspan="2"><img src="/images/trans.gif" height=15></td></tr>
<tr>
<td valign="top"><img src="/images/qa-q.gif" border=0></td>
<td>我知道婴儿吃蜂蜜有一定的安全隐患，但是婴儿的母亲可不可以吃蜂蜜？细菌孢子会不会通过乳液传给婴儿？若婴儿已经检测出感染了婴儿肉毒症，请问母亲还应不应该继续母乳喂养？</td>
</tr>

<tr>
<td valign="top"><img src="/images/qa-a.gif" border=0></td>
<td>
婴儿的母亲是可以食用蜂蜜的，并且细菌孢子不会通过乳液传给婴儿。婴儿在接受治疗和恢复过程中，建议母亲继续母乳喂养婴儿。

<br /><br />
勤洗手，尤其是家庭成员经常食用蜂蜜的情况下，是至关重要的。这可以有效防护婴儿的嘴巴接触到任何有蜂蜜残留的地方。

<br /><br />
母亲食用蜂蜜时，尽管蜂蜜中可能含有细菌孢子，这些孢子不会对母亲产生任何影响，也因为孢子个体体积太大不会通过乳液传给婴儿。肉毒杆菌毒素同样也不会通过乳液传给婴儿。因此，母乳不是一个导致婴儿感染婴儿肉毒症的病因。

<br /><br />
如网站中的“病人管理”页面所述，母乳中含有的营养对婴儿来说是非常充足的，所以建议不管婴儿有没有感染婴儿肉毒症，母亲都坚持母乳喂养婴儿。
<br /><br />

若需要更详细的母乳喂养和婴儿肉毒症介绍，请参考“预防措施” 页面。

</td>
</tr>

