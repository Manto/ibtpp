<tr>
<td valign="top"><img src="/images/qa-q.gif" border=0></td>
<td>我应该怎么处理康复婴儿的尿布？</td>
</tr>

<tr>
<td valign="top"><img src="/images/qa-a.gif" border=0></td>
<td>
现在已知婴儿肉毒症患者的大便中会含有肉毒梭状芽孢杆菌孢子以及肉毒杆菌毒素，往往持续数个星期至数个月。因此，更换尿布后洗手是一项必备的习惯。换下的尿布应被立即丢弃至没有人或动物可以触碰到的地方。若手上有划伤，则需要在换尿布时带上手套。

<br /><br />
因为患者的大便中会含有孢子和毒素，建议在这段时间减少和别的婴儿的接触时间。这是为了保证若尿布破了或漏了，别的婴儿不会接触到这些有害物。患者与其他婴儿接触的这段时间应有成人在一边监督。

<br /><br />
肉毒梭状芽孢杆菌并不是一种人体内的良性细菌，所以会在患者完成治疗后逐渐从大便中消失，直到完全清除。
</td>
</tr>

