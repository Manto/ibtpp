BabyBIG<sup>&reg;</sup>, (Humano) Botulismo Inmune Globulin Intravenoso (BIG-IV) es una medicina huérfana que consiste en anticuerpos de antitoxina botulínica de origen humano que ha sido aprobada por la Administración de Drogas y Alimentos de los Estados Unidos (FDA) para el tratamiento de botulismo infantil de tipos A y B.
<br /><br />
El producto ha sido tratado como un detergente-solvente, estéril, polvo liofilizado de inmunoglubulin G (IgG), estabilizado con el 5% de sacarosa y el 1% de albúmina (humana).  No contiene preservativos.  El inmunoglobulin purificado se deriva del plasma de personas inmunizadas con el toxoide pentavalente de botulinum que fueron seleccionadas por sus altos títulos de anticuerpos neutralizadores contra las neurotoxinas de botulinum de tipos A y B.  Todos los donadores fueron examinados y dieron resultados negativos a los anticuerpos contra el virus humano de inmunodeficiencia (HIV) y los virus de hepatitis B y hepatitis C.  El plasma acumulado fue fraccionado con una precipitación de etanol frío de las proteínas de acuerdo al método de Cohn/Oncley, modificado para producir un producto adecuado para la administración intravenosa.

