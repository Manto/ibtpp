<tr>
<td valign="top"><img src="/images/qa-q.gif" border=0></td>
<td>How should I handle my baby's diapers as he/she recovers from botulism?</td>
</tr>

<tr>
<td valign="top"><img src="/images/qa-a.gif" border=0></td>
<td>
It is known that patients with infant botulism excrete both <i>C. botulinum</i> toxin and organism
in their feces for periods ranging from weeks to months after symptom onset. Consequently,
scrupulous handwashing should be practiced after each diaper change. Soiled diapers should be
quickly disposed of in an area where no other person or animal can come into contact with them.
Persons with open cuts or wounds on their hands should wear gloves when changing diapers.
<br /><br />
Because the patient may be excreting the toxin and organism for weeks to months, it is
advisable to limit close contact with other infants and children during this time. This is to ensure
that other children do not come into contact with fecal material from a leaky diaper. Any
contact the patient has with other infants and children during this time should be supervised by an adult.
<br /><br />
<i>C. botulinum</i> is not part of the patient's normal flora (normal, healthy bacteria that live in our
bodies) and will eventually stop being excreted in the infant's feces.
</td>
</tr>

