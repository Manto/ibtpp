<td>How is infant botulism treated?</td>
</tr>

<tr>
<td valign="top"><img src="/images/qa-a.gif" border=0></td>
<td>
Infant botulism is treated with meticulous supportive care with special attention to feeding and breathing needs. In the United States the orphan drug BabyBIG<sup>&reg;</sup>  is also used to shorten hospital stay and reduce complications.
</td>
</tr>

<tr><td colspan="2"><img src="/images/trans.gif" height=15></td></tr>
<tr>
<td valign="top"><img src="/images/qa-q.gif" border=0></td>
<td>What is BabyBIG<sup>&reg;</sup>?</td>
</tr>

<tr>
<td valign="top"><img src="/images/qa-a.gif" border=0></td>
<td>The orphan drug BabyBIG<sup>&reg;</sup> is human-derived botulism antitoxin that was approved (licensed) by the U.S. Food and Drug Administration (FDA) for the treatment of infant botulism on October 23, 2003.  Use of BabyBIG<sup>&reg;</sup> significantly reduces the length of hospital stay and associated hospital costs in patients with infant botulism.</td>
</tr>

<tr><td colspan="2"><img src="/images/trans.gif" height=15></td></tr>
<tr>
<td valign="top"><img src="/images/qa-q.gif" border=0></td>
<td>How soon after BabyBIG<sup>&reg;</sup> treatment can my child be immunized?</td>
</tr>

<tr>
<td valign="top"><img src="/images/qa-a.gif" border=0></td>
<td>he infant should have good to full recovery of muscle strength and tone before immunizations resume. In addition, most live-virus vaccines (i.e., measles, mumps, rubella and varicella) will need to be delayed until 6 months after BabyBIG<sup>&reg;</sup> treatment because the antibodies in BabyBIG<sup>&reg;</sup> may interfere with the effectiveness of the vaccine.
<br /><br />
The live-virus vaccines for rotavirus, RotaTeq<sup>&reg;</sup> and Rotatrix<sup>&reg;</sup> should also be delayed until 6 months after BabyBIG<sup>&reg;</sup> treatment. Although current data suggest that administration of intravenous immunoglobulin products such as BabyBIG<sup>&reg;</sup> will not interfere with the efficacy of oral rotavirus vaccines, patients with infant botulism should not receive the rotavirus vaccine because of the slowed intestinal motility that results from infant botulism.
<br /><br />
Accordingly, any of the recommended doses of the rotavirus vaccine (RotaTeq<sup>&reg;</sup> or Rotarix<sup>&reg;</sup>) that were not given to the infant before treatment with BabyBIG<sup>&reg;</sup> should be delayed. Because the other live-virus vaccines (i.e., measles, mumps, rubella and varicella) are normally first given at one year of age, only those infant botulism patients who were 6 months of age or older when they were treated with BabyBIG<sup>&reg;</sup> will need delayed immunization with these vaccines.
</td>
</tr>


<tr><td colspan="2"><img src="/images/trans.gif" height=15></td></tr>
<tr>
<td valign="top"><img src="/images/qa-q.gif" border=0></td>
<td>Are there any long-term consequences of infant botulism?</td>
</tr>

<tr>
<td valign="top"><img src="/images/qa-a.gif" border=0></td>
<td>
In the absence of serious hospital-acquired complications, no. The prognosis for infant botulism patients is excellent with anticipated full and complete recovery. Recovery results from regrowth of the nerve endings that then are able to signal the muscles to contract. Botulinum toxin does not penetrate into the brain, and so infant botulism patients retain all the intelligence, athletic ability, musical ability, sense of humor and orneriness with which they were born.
</td>
</tr>

<tr><td colspan="2"><img src="/images/trans.gif" height=15></td></tr>
<tr>
<td valign="top"><img src="/images/qa-q.gif" border=0></td>
<td>Can my baby get infant botulism again?</td>
</tr>

<tr>
<td valign="top"><img src="/images/qa-a.gif" border=0></td>
<td>
No. Infants treated with BabyBIG<sup>&reg;</sup> will have a protective level of toxin-neutralizing antibody for at least six months following administration of the medicine.  This feature allows sufficient time for elimination of <i>C. botulinum</i>, which has temporarily colonized the infant's intestine causing disease.
<br /><br />
Since the disease of infant botulism was first recognized more than 40 years ago, there have been no instances of an infant acquiring the disease more than once. During this time more than 4400 cases of infant botulism are known to have occurred worldwide.
<br /><br />
However, a few reports of "relapsing" infant botulism have been published during this time.  Careful review of these case reports by our program's physicians indicates that these were cases in which the infant was discharged prematurely, before they had adequate recovery of strength needed to sustain feeding and breathing. For a further discussion of this topic please see the patient management section of our website under the For Physicians tab. 
</td>
</tr>



