<i>Clostridium botulinum</i> is a spore-forming, obligately anaerobic, Gram-positive bacillus that produces a heat-labile neurotoxin. This organism is divided into four physiological groups that produces seven antigenically distinct toxin types, which are classified as botulinum toxin types A – G.   Botulinum toxin is the most potent toxin known.  The mode of action of botulinum toxin is to cleave key proteins that are necessary for the release of acetylcholine into the neuromuscular junction. The end result of the toxemia is a descending, bilateral, symmetric, flaccid paralysis.  Hallmark clinical features of botulism include: ptosis (droopy eyelids), diplopia (double vision), slurred speech due to paralysis of the tongue muscles, difficulty swallowing, diminished reflexes and hypotonia.  Complete paralysis of the airway and respiratory muscles may lead to respiratory arrest and death.
<br><br>
Strains of two other clostridial species have acquired the gene to produce botulinum toxin.  Very rare strains of <i>C. butyricum</i> and <i>C. baratii</i> have been found to produce botulinum toxin type E and toxin type F, respectively. Most of these rare toxigenic <i>C. butyricum</i> and <i>C. baratii</i> strains were discovered because they caused intestinal (infant) botulism.

