<h3><b><i><u>For California patients only:</u></i></b></h3>

<span class="emphasis" style="color: #FF3333"><b>
Stool, enema or rectal wash specimens from California inpatients or outpatients with suspected IB may be submitted to the IBTPP laboratory. Physicians seeking testing for their patients should first call the IBTPP on-call physician at (510) 231-7600 prior to specimen submission to provide case information and review indications for testing. Testing will not be conducted without a prior clinical consultation with the IBTPP on-call physician.</b></span><br><br>

For all other states, please contact your state public health agency for specimen submission instructions. 

<ol>
<li>Tightly seal cap on collection container and package the primary container of stool or enema in a zip-top specimen bag. </li>
<li>Place your laboratory's test requisition form in separate pouch or second bag. </li>
<li>Follow IATA regulations and package the specimen in a styrofoam box with one or more cold-packs. Label box with a <b>UN 3373</b>, "Biological Substance Category B" label. </li>
<li>Expedite specimen shipment by using a local courier for same day delivery before 2pm, or by using a major service for "priority overnight" delivery. Packages should be shipped for overnight delivery <b>Monday through Thursday</b> only.  Our laboratory does not accept deliveries on Saturday or Sunday.</li>

</ol>

Send specimens directly to California Department of Public Health.  <u><b>Do not</b> send through your local county public health laboratory as this may delay testing</u>. Address the package exactly as below: 

<p>
<blockquote>
	Infant Botulism Laboratory - IBTPP<br>
	California Department of Public Health<br>
	Specimen Receiving, Room B106<br>
	850 Marina Bay Parkway<br>
	Richmond, CA 94804<br><br>

	Phone number: (510) 231-7600
</blockquote>
</p>	

<b>IMPORTANT: </b> Properly identify and label all packages that contain diagnostic specimens when submitting them to the IBTPP laboratory. Always include the sender's name and submitting institution's name and address. Improperly identified packages may be subject to destruction for bio-security reasons. 


