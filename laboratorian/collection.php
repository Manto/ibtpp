<span class="emphasis" style="color: #FF3333"><b>
Stool or enema specimens from California inpatients or outpatients with suspected IB must be submitted directly to the IBTPP laboratory. Physicians seeking testing for their patients should first call the IBTPP on-call physician at (510) 231-7600 prior to specimen submission to provide case information and review indications for testing.
<br /><br />
Testing will not be initiated without prior clinical consultation with the IBTPP on-call physician.
</b></span>
<br /><br />
<span><b><u>
The specimen required for the definitive diagnosis of infant botulism is stool or enema. 
</b></u></span>

<br /><br />

The best container in which to collect, store and submit fecal specimens is a sterile urine container with a tight, screw-capped lid. <b><u><i>Do not use</i></u></b> containers containing fixatives or preservatives. If spontaneously passed stool is difficult to obtain due to constipation, an attempt to collect stool in the rectal vault should be made by <b><i>gentle</i></b> digital examination. If no stool can be obtained digitally, do not wait for a spontaneous bowel movement. Instead, please follow the enema collection procedure outlined below.

<br><br>
<b>Important</b>: 
<i>Glycerin suppositories yield an unsatisfactory specimen and <b>should not be</b> used. The procedure described below will yield the best enema specimen for diagnostic testing.
</i>

<br><br>

<b style="font-size:18px;">Enema Collection Procedure</b>
<ol>

<li>Attach a 12 to 16 French red rubber (Robinson) catheter to a tapered, catheter-tip syringe.</li>
<li>Trim catheter tip to enlarge hole.</li>
<li>Lubricate the catheter tip with petroleum jelly or equivalent and insert into distal colon.</li>
<li>The volume of sterile, <i><b>non-bacteriostatic</b></i> water to use should be a bedside clinical decision based on the patient's body mass.</li>
<li>Inject <b>up to</b> 30 ml of sterile, <i><b>non-bacteriostatic</b></i> water slowly into distal colon and maintain catheter in rectum. Please note that a <i><u>minimum volume of 6 ml</u></i> is required to enable the most accurate diagnostic analysis.</li>
<li>Wait approximately 3 minutes, and then draw enema effluent into the syringe.</li>
<li>Have an assistant hold a sterile urine container under the anus during this time to collect any expelled material.</li>
<li>Expel all fluid collected in the syringe into the same sterile urine container.</li>
<li>Tightly seal the lid. Properly label the container with patient's name, date and time of collection.</li>
<li>If more than 5 ml of water is retained in the colon, exert gentle pressure onto left lower abdomen (with your hand or with infant's knee to abdomen) to aid in excretion and to minimize intestinal absorption of water.</li>
<li>Send the enema specimen to your laboratory with instructions to keep it refrigerated and to expedite shipment to the appropriate botulism diagnostic laboratory.</li>

</ol>

<br><br>

Please retain all subsequent stool specimens and have your laboratory store them in their refrigerator until a diagnosis has been established by the appropriate laboratory. Please do not send <b><i><u>extra</u></i></b> stool specimens unless requested.

<br /><br />

<b>For California only</b> - please contact 510-231-7676 with any questions regarding specimen collection, storage or submission requirements. For all other states, please contact your local public health agency. Thank you. 

