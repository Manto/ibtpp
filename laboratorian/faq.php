<td>How long does it take to receive results?</td>
</tr>
<tr>
<td valign="top"><img src="/images/qa-a.gif" border=0></td>
<td>Direct toxin results are often, but not always, available the morning  	following receipt of the specimen.  Results of the stool culture for <i>C. botulinum</i> may take as little as one-week  or as long as one-month to 	complete and varies from case to case.  IBTPP will notify the patient's physician by phone as soon as the direct toxin results are available.  A 	final report is issued once the full analysis is complete; this report is sent 	to the submitting laboratory.</td>
</tr>

