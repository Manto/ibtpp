</tr>
<tr>
<td valign="top"><img src="http://www.infantbotulism.org/images/qa-a.gif" border=0></td>
<td>No.  Please send all diagnostic stool specimens from California patients directly to the IBTPP laboratory at CDPH (please see laboratory information pages).  Please note that laboratory testing will be conducted <span style="text-decoration: underline; font-weight: bold;">only after</span> the physician caring for the patient has consulted with the IBTPP on-call physician.  Do not submit "unannounced" specimens as they will not be tested.   Thank you.</td>
