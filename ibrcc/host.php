This year's IBRCC is hosted by The Infant Botulism Treatment and
Prevention Program (IBTPP) of the California Department of Public
Health, located in Richmond, California. 
<br /><br />
The mission of the IBTPP is to provide and improve the treatment of
infant botulism and related diseases.  IBTPP provides diagnostic and
treatment consultation to physicians inside and outside of the United
States who suspect their patients may have infant botulism.  For
suspected and confirmed cases, IBTPP provides treatment in the form of
BabyBIG(r), Botulism Immune Globulin Intravenous (Human) (BIG-IV), an
orphan drug that consists of human-derived botulism antitoxin
antibodies.  BabyBIG(r) has been approved by the United States Food and
Drug Administration for the treatment of infant botulism types A and B.
