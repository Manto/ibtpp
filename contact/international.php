<p>
The Infant Botulism Treatment and Prevention Program is available 24 hours a day to provide clinical consultation on cases of suspected or confirmed infant botulism. Consultation is available as a public service, regardless of whether or not a clinician wishes to obtain BabyBIG<sup>&reg;</sup>.
</p>

<p>
For BabyBIG<sup>&reg;</sup> requests and/or clinical consultation outside of the United States (US), please contact the Infant Botulism Treatment and Prevention Program (IBTPP) at the 24/7/365 phone number 1-510-231-7600 and ask to speak to the doctor on-call.
</p>

<p>
BabyBIG<sup>&reg;</sup> is available internationally on a case-by-case evaluation basis.
</p>

<p>
<u>International requests for BabyBIG<sup>&reg;</sup>:</u>

<ul>
<li>Access to BabyBIG<sup>&reg;</sup> is authorized only by one of the IBTPP on-call physicians, who must be contacted by the patient's attending physician to discuss the patientâ€™s history and clinical findings before BabyBIG<sup>&reg;</sup> can be shipped.</li>
<li>An IBTPP on-call physician can be reached 24 hours a day, 7 days a week at exit calling code -1-510-231-7600.</li>
<li>Please be advised that the attending physician or hospital must obtain approval from their country's Medicines Regulatory Authority to import BabyBIG<sup>&reg;</sup> because BabyBIG<sup>&reg;</sup> is licensed only in the US.</li>
</ul>
</p>


<p>
  <u>
    Please be advised that the following steps are required for shipment and importation of BabyBIG<sup>&reg;</sup>:
  </u>
<ul>
  <li>A letter of authorization to import must be provided to the Infant Botulism Treatment and Prevention Program (IBTPP) by the requesting country's Medicines Regulatory Authority or Agency before BabyBIG<sup>&reg;</sup> is released for shipment.
  </li>
  <li>BabyBIG<sup>&reg;</sup> will have to clear customs after arrival in the importing country. Arrangements to expedite customs clearance should be initiated as soon as BabyBIG<sup>&reg;</sup> is shipped.</li>
  <li>In addition to paying the statutorily required fee to CDPH for BabyBIG<sup>&reg;</sup>, the receiving hospital will also have to pay for the shipment of BabyBIG<sup>&reg;</sup> via an expedited courier service and for any customs charges.</li>
  <li>
      Before BabyBIG<sup>&reg;</sup> is released for shipment, IBTPP must receive:

      <ol>
        <li>
            the signed Invoice and Purchase Agreement (IPA) document, which is available here, <a href="http://infantbotulism.org/physician/IPA-International-2021-04-v2.pdf">International Invoice and Purchase Agreement</a> (last updated April 2021);
        </li>
        <li>
            medical records submitted either electronically or by fax that include documentation of the patient's weight, and;
        </li>
        <li>
            the import permit authorization letter from the requesting country's Medicines Regulatory Agency.
          </li>
      </ol>
  </li>
</ul>
</p>

<p>
    <u>
        International use of BabyBIG<sup>&reg;</sup>:
    </u>

    <ul>
      <li>
          As of February 2021, BabyBIG<sup>&reg;</sup> has been used to treat infants in 19 countries<sup>1</sup>, spanning 5 continents. If used, BabyBIG<sup>&reg;</sup> should be given as soon as possible in the patient's illness. Treatment should not be delayed while waiting for laboratory confirmation of the diagnosis. Treatment within the first 3 days of hospitalization shortens the hospital stay the most. Treatment between hospital days 4 through 7 also significantly shortens hospital stay when compared to placebo<sup>2,3</sup>, (N Engl J Med 2006;354:462-71) (J Pediatr 2018;193:172-7).
      </li>
  </ul>
</p>

